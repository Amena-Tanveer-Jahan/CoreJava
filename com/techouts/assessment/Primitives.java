package com.techouts.assessment;

public class Primitives {
    byte byteNum;
    short shortNum;
    int intNum;
    long longNum;
    float floatNum;
    double doubleNum;
    char charValue;
    boolean booleanValue;

    public static void main(String[] args){
        Primitives obj = new Primitives();
        System.out.println(obj.byteNum);
        System.out.println(obj.shortNum);
        System.out.println(obj.intNum);
        System.out.println(obj.longNum);
        System.out.println(obj.floatNum);
        System.out.println(obj.doubleNum);
        System.out.println(obj.charValue);
        System.out.println(obj.booleanValue);
    }


}
